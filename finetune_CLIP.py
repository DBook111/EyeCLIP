def build_transform(is_train, args):
    mean = IMAGENET_DEFAULT_MEAN
    std = IMAGENET_DEFAULT_STD
    # train transform
    if is_train=='train':
        # this should always dispatch to transforms_imagenet_train
        transform = create_transform(
            input_size=args.input_size,
            is_training=True,
            color_jitter=args.color_jitter,
            auto_augment=args.aa,
            interpolation='bicubic',
            re_prob=args.reprob,
            re_mode=args.remode,
            re_count=args.recount,
            mean=mean,
            std=std,
        )
        return transform

    # eval transform
    t = []
    if args.input_size <= 224:
        crop_pct = 224 / 256
    else:
        crop_pct = 1.0
    size = int(args.input_size / crop_pct)
    t.append(
        transforms.Resize(size, interpolation=transforms.InterpolationMode.BICUBIC), 
    )
    t.append(transforms.CenterCrop(args.input_size))
    t.append(transforms.ToTensor())
    t.append(transforms.Normalize(mean, std))
    return transforms.Compose(t)

class downstream_dataset(Dataset):
    def __init__(self, root_path, split):
        image_path = []
        label = []
        for i,c in enumerate(os.listdir(os.path.join(root_path, split))):
            #print(c)
            for p in os.listdir(os.path.join(root_path, split, c)):
                imgp = os.path.join(root_path, split, c, p)
                #print(imgp)
                image_path.append(imgp)
                label.append(i)
        self.image_path = image_path
        self.label = label

    def __len__(self):
        return len(self.image_path)
    
    def __getitem__(self, idx):
        image = preprocess(Image.open(self.image_path[idx])) # Image from PIL module
        #print(image1.shape)
        l = self.label[idx]
        #print(image2.shape)
        #title = self.title[idx]
        return image, l

